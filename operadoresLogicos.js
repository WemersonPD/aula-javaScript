// Tipos:

// && (and - e) - Só é verdadeiro caso as duas partes envolvidas sejam verdadeiras. 
// exemplo:
    // true  && false igual a false
    // true && true igual a true

// || (or - ou) - É verdadeiro se um ou mais partes forem verdadeiras.
// exemplo:
    // true  || false igual a true
    // false || false igual a false


// ! (not - negação) - Inverte o valor.
// exemplo:
    // !(true  && false) igual a  true, pois (true && false) retorna false.
    // !(false || false) igual a true, pois (false || false) retorna false.
