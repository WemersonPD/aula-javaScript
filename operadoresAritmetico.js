// Operadores aritmeticos avançados:

// var++ (poscremento) / var--
let poscremento = 50;
console.log(poscremento++); //Vai add 1 a váriavel após a execução do comando 
console.log(poscremento); //Aqui a var valera 51

// ++var (precremento) / --var
let precremento = 30;
console.log(++precremento) //Já nesse comand, a variavel valera 31